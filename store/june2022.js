export const mutations = {
  updateTodo: (state, payload) => {
    state.todos[payload].todo = state.todos[payload].todo ? false : true
  },
  resetTodo (state) {
    state.todos.forEach(element => {
      element.todo = false
    })
  }
}

export const actions = {
  todoCheckAction (context, payload) {
    context.commit('updateTodo', payload)
  },
  resetTodosAction (context) {
    context.commit('resetTodo')
  }
}

export const getters = {
  getToDos (state) {
    return state.todos
  },
  doneTodos (state) {
    return state.todos.filter(todo => todo.todo)
  }
}
